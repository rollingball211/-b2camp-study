package pmmd.p1;

public class Calculator {

  
  static int plus(int result, int value) {
    return result + value;
  }
  
  static int minus(int result, int value) {
    return result - value;
  }
  
  static int multiple(int result, int value) {
    return result * value;
  }
  
  static int divide(int result, int value) {
    return result / value;
  }
}
